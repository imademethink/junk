	package Encapsulation;
	
	public class Set_Get {
	
		private int age;
		
		private String name;
		
		public void  setage(int age) {
			
		this.age=age;
		
		}
		public int getage() {
			
			return age;
		
		}
		public void setname(String name) {
			
			this.name=name;
		}
		public String getname() {
			
			return name;
		}
		
	}
	